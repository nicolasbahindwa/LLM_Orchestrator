# ══════════════════════════════════════════════════════════════════════════

@dataclass
class RoutingContext:
    """
    Context information for making routing decisions.

    Contains everything the router needs to pick the best provider.

    Attributes:
        request: The original LLM request
        available_providers: Which providers are currently usable
        provider_metrics: Current metrics for each provider
        constraints: User-specified constraints (max cost, max latency, etc.)

    The router uses this to answer:
    - Which providers can handle this request?
    - Which provider best matches the user's constraints?
    - Which provider gives the best balance of cost/quality/speed?
    """

    request: LLMRequest
    available_providers: List[str]
    provider_metrics: Dict[str, ProviderMetrics]
    constraints: Dict[str, Any] = field(default_factory=dict)
