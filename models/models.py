# PROVIDER MODELS - Information about LLM providers
# ══════════════════════════════════════════════════════════════════════════

@dataclass
class ProviderMetrics:
    """
    Real-time metrics for a provider.

    Tracks performance and health to make smart routing decisions.

    Attributes:
        provider_name: Name of the provider (e.g., "anthropic")
        status: Current health status
        success_rate: Percentage of successful requests (0.0-1.0)
        avg_latency_ms: Average response time
        error_count: Number of recent errors
        total_requests: Total requests processed
        last_success: When last successful request completed
        last_error: When last error occurred

    Usage:
        The orchestrator uses these metrics to decide:
        - Should I use this provider? (status check)
        - Which provider is fastest? (avg_latency_ms)
        - Which provider is most reliable? (success_rate)
    """

    provider_name: str
    status: ProviderStatus = ProviderStatus.HEALTHY

    # Performance metrics
    success_rate: float = 1.0  # 1.0 = 100% success
    avg_latency_ms: float = 0.0

    # Error tracking
    error_count: int = 0
    total_requests: int = 0

    # Timestamps
    last_success: Optional[datetime] = None
    last_error: Optional[datetime] = None

    def is_healthy(self) -> bool:
        """Check if provider is healthy enough to use"""
        return self.status in [ProviderStatus.HEALTHY, ProviderStatus.DEGRADED]

    def update_success(self, latency_ms: float):
        """Record a successful request"""
        self.total_requests += 1
        self.last_success = datetime.now()

        # Update rolling average latency
        if self.total_requests == 1:
            self.avg_latency_ms = latency_ms
        else:
            # Weighted average: 80% old, 20% new
            self.avg_latency_ms = (0.8 * self.avg_latency_ms) + (0.2 * latency_ms)

        # Update success rate
        successful_requests = self.total_requests - self.error_count
        self.success_rate = successful_requests / self.total_requests

    def update_error(self):
        """Record a failed request"""
        self.total_requests += 1
        self.error_count += 1
        self.last_error = datetime.now()

        # Update success rate
        successful_requests = self.total_requests - self.error_count
        self.success_rate = successful_requests / self.total_requests if self.total_requests > 0 else 0.0


